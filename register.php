	<script type="text/javascript">
        $(document).ready(function () {

            $("#register").click(function () {

                fname = $("#fname").val();
                lname = $("#lname").val();
                email = $("#email").val();
                password = $("#password").val();

                $.ajax({
                    type: "POST",
                    url: "adduser.php",
                    data: "fname=" + fname + "&lname=" + lname + "&email=" + email + "&password=" + password,
                    success: function (html) {
                        if (html == 'true') {

                            $("#add_err2").html('<div class="alert alert-success">Account processed.</div>');

                            window.location.href = "index.php";

                        } else if (html == 'false') {
                            $("#add_err2").html('<div class="alert alert-danger">Email Address already in system.</div>');                    

                        } else if (html == 'fname') {
                            $("#add_err2").html('<div class="alert alert-danger">First Name is required.</div>');
												 
						} else if (html == 'lname') {
                            $("#add_err2").html('<div class="alert alert-danger">Last Name is required.</div>');

                        } else if (html == 'eshort') {
                            $("#add_err2").html('<div class="alert alert-danger">Email Address is required.</div>');

                        } else if (html == 'eformat') {
                            $("#add_err2").html('<div class="alert alert-danger">Email Address format is not valid.</div>');
												 
						} else if (html == 'pshort') {
                            $("#add_err2").html('<div class="alert alert-danger">Password must be at least 4 characters.</div>');

                        } else {
                            $("#add_err2").html('<div class="alert alert-danger">Error processing request. Please try again.</div>');
                        }
                    },
                    beforeSend: function () {
                        $("#add_err2").html("loading...");
                    }
                });
                return false;
            });
        });
    </script>

